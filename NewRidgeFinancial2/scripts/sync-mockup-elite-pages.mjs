#!/usr/bin/env node
/** Regenerate site/data/mockup-elite-pages.js from page_mockups_elite/*.html */
import { readdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const eliteDir = join(root, "..", ".local_logs", "moonshot_financial_eval", "page_mockups_elite");
const outPath = join(root, "site", "data", "mockup-elite-pages.js");
const PAGE_ID_RE = /^[a-z0-9]+(?:-[a-z0-9]+)*$/;

const pages = readdirSync(eliteDir)
  .filter((name) => name.endsWith(".html") && name !== "index.html")
  .map((name) => name.replace(/\.html$/, ""))
  .filter((id) => PAGE_ID_RE.test(id))
  .sort();

const body = `/** Auto-generated by scripts/sync-mockup-elite-pages.mjs — do not edit by hand. */
(function (root) {
  root.__NR2_MOCKUP_ELITE_PAGES = ${JSON.stringify(pages, null, 2)};
})(typeof window !== "undefined" ? window : globalThis);
`;

writeFileSync(outPath, body, "utf8");
console.log(`mockup-elite-pages.js updated (${pages.length} pages): ${pages.join(", ")}`);
