function defineWord(word) {
  const w = word.toLowerCase();
  if (COMMON_DEFS[w]) return COMMON_DEFS[w];
  if (w.length <= 2) return "n. very short English word";
  if (w.length <= 4) return "n. common English word";
  if (w.endsWith("ly")) return "adv. manner adverb";
  if (w.endsWith("ing")) return "v. or adj. present participle form";
  if (w.endsWith("ed")) return "v. past tense form";
  if (w.endsWith("tion") || w.endsWith("sion")) return "n. noun formed with -tion/-sion";
  if (w.endsWith("ness")) return "n. state or quality";
  if (w.endsWith("able") || w.endsWith("ible")) return "adj. capable of";
  return `n. English vocabulary word (${w.length} letters)`;
}

const raw = readFileSync(wordsPath, "utf8");
const words = raw
  .split(/\r?\n/)
  .map((w) => w.trim().toLowerCase())
  .filter((w) => w && /^[a-z]+$/.test(w));

console.log(`Indexing ${words.length} words…`);

const docs = [];
const detailById = {};
const now = new Date().toISOString().slice(0, 10);

for (let i = 0; i < words.length; i += WORDS_PER_VOLUME) {
  const slice = words.slice(i, i + WORDS_PER_VOLUME);
  const first = slice[0];
  const last = slice[slice.length - 1];
  const vol = String(Math.floor(i / WORDS_PER_VOLUME) + 1).padStart(3, "0");
  const title = `English Dictionary Vol ${vol} (${first}–${last})`;
  const lines = slice.map((w) => `${w}: ${defineWord(w)}`);
  const content = lines.join("\n");
  const entry = {
    title,
    type: "Dictionary",
    category: "English",
    size: `${Math.round(content.length / 1024)} KB`,
    updated: now,
    by: "HAL English dictionary",
    tags: ["english", "dictionary", "vocabulary", `vol-${vol}`],
    excerpt: content.slice(0, 4000),
    content,
  };
  docs.push(entry);
  detailById[title] = {
    title: entry.title,
    type: entry.type,
    size: entry.size,
    updated: entry.updated,
    docType: "Dictionary",
    tags: entry.tags,
    uploadedBy: entry.by,
    dateAdded: entry.updated,
    path: `/library/${title.toLowerCase().replace(/[^a-z0-9]+/g, "-")}.txt`,
    body: content,
  };
}

const library = {
  results: docs.length,
  storage: {
    indexed: docs.length,
    wordCount: words.length,
    source: "english-words-alpha.txt",
    refreshedAt: new Date().toISOString(),
    volumes: docs.length,
  },
  filters: ["All", "Dictionary", "English"],
  docs,
  detailById,
  wordIndex: {
    count: words.length,
    randomSample: words.filter((_, idx) => idx % 137 === 0).slice(0, 5000),
  },
};

writeFileSync(outPath, JSON.stringify(library));
console.log(`Wrote ${docs.length} volumes (${words.length} words) → ${outPath}`);
